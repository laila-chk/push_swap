#include "push_swap.h"

void	ft_swap(int *i)
{
	int k;
	k = i[0];
	i[0] = i[1];
	i[1] = k;
}

void ft_push(int *i, int *j, t_vars sz)
{
	int		k;

	k = sz.size;
	while (k > 0)
	{
		j[k] = j[k - 1];
		k--;
	}
	void(*i);
}
